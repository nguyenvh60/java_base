package Base;

public class Bai8_ToanTuSoSanh {
    public static void main(String[] args) {
        //Toán tử so sánh được sử dụng để so sánh hai giá trị (hoặc biến).
        //  giúp chúng ta tìm ra câu trả lời và đưa ra quyết định.

        //Giá trị trả về của phép so sánh là hoặc true. false Những giá trị này được gọi là giá trị Boolean

        int x = 5;
        int y = 3;
        System.out.println(x > y); // trả về true vì 5>3 , ngược lại là false


        // 	Toán tử so sánh != để so sánh xem 2 giá trị có khác nhau không, nếu khác (không bằng) là true
        int x1 = 5;
        int y2 = 3;
        System.out.println(x1!= y2); // trả về true vì 5 không bằng 3

        // Một số phép so sánh khác ( > , < , >= , <= ) tương tự
    }
}
