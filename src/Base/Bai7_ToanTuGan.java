package Base;

public class Bai7_ToanTuGan {
    public static void main(String[] args) {
        int x = 10 ; // sử dụng toán tử gán (=) để gán giá trị 10 cho một biến có tên là x
        System.out.println(x);


        // Toán tử gán cộng ( +=) thêm một giá trị vào một biến:
        int x1 = 10 ;
        x1 += 5 ;
        System.out.println(x1);
    }
}
